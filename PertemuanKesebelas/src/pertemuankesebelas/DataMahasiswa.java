    private void btndelete2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndelete2ActionPerformed
        // TODO add your handling code here:                                           
        String nim = txtnim2.getText().trim();
        if (nim.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pilih/masukkan NIM yang akan dihapus terlebih dahulu!");
            return;
        }

        // Konfirmasi penghapusan
        int confirm = JOptionPane.showConfirmDialog(this,
                "Yakin ingin menghapus data dengan NIM = " + nim + " ?",
                "Konfirmasi Hapus",
                JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        EntityManagerFactory emf = null;
        EntityManager em = null;

        try {
            emf = Persistence.createEntityManagerFactory("PertemuanKesebelasPU");
            em = emf.createEntityManager();
            em.getTransaction().begin();

            Mahasiswa m = em.find(Mahasiswa.class, nim);
            if (m == null) {
                JOptionPane.showMessageDialog(this, "Data dengan NIM " + nim + " tidak ditemukan!");
                em.getTransaction().rollback();
                return;
            }

            em.remove(m);
            em.getTransaction().commit();

            JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");

            loadData();

            txtnim2.setText("");
            txtnama.setText("");
            txtgender2.setText("");
            txtprodi2.setText("");
            txtalamat2.setText("");
            txtttl2.setText("");

        } catch (Exception ex) {
            if (em != null && em.getTransaction().isActive()) {
                em.getTransaction().rollback();
            }
            JOptionPane.showMessageDialog(this, "Gagal menghapus data: " + ex.getMessage());
            ex.printStackTrace();
        } finally {
            if (em != null) {
                em.close();
            }
            if (emf != null) {
                emf.close();
            }
        }
        fromdelete.dispose();
    }//GEN-LAST:event_btndelete2ActionPerformed

