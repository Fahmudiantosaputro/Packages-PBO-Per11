    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        try {
            if (txtnim.getText().equals("")
                    || txtnama.getText().equals("")
                    || cbgender.getSelectedItem() == null
                    || txtprodi.getText().equals("")
                    || txtalamat.getText().equals("")
                    || txtttl.getText().equals("")) {

                JOptionPane.showMessageDialog(this, "Isi semua data terlebih dahulu!");
                return;
            }

            String nim = txtnim.getText();
            String nama = txtnama.getText();
            String gender = cbgender.getSelectedItem().toString();
            String prodi = txtprodi.getText();
            String alamat = txtalamat.getText();
            String ttl = txtttl.getText();

            EntityManagerFactory emf = Persistence.createEntityManagerFactory("PertemuanKesebelasPU");
            EntityManager em = emf.createEntityManager();

            Mahasiswa existing = em.find(Mahasiswa.class, nim);
            if (existing != null) {
                JOptionPane.showMessageDialog(this, "NIM sudah terdaftar!");
                em.close();
                emf.close();
                return;
            }

            em.getTransaction().begin();

            Mahasiswa m = new Mahasiswa();
            m.setNim(nim);
            m.setNama(nama);
            m.setJenisKelamin(gender);
            m.setProdi(prodi);
            m.setAlamat(alamat);
            m.setTtl(ttl);

            em.persist(m);
            em.getTransaction().commit();
            em.clear();

            JOptionPane.showMessageDialog(this, "Data berhasil disimpan!");

            loadData();

            txtnim.setText("");
            txtnama.setText("");
            cbgender.setSelectedIndex(-1);
            txtprodi.setText("");
            txtalamat.setText("");
            txtttl.setText("");

            em.close();
            emf.close();

        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan data: " + e.getMessage());

        }
        frominsert.dispose();
    }//GEN-LAST:event_btnsaveActionPerformed

